package com.educate.petsapp.data;

public class Pets {
    public int id;
    public String name;
    public String breed;
    public int gender;
    public int weight;

    public Pets(int id, String name, String breed, int gender, int weight) {
        this.id = id;
        this.name = name;
        this.breed = breed;
        this.gender = gender;
        this.weight = weight;
    }
}
